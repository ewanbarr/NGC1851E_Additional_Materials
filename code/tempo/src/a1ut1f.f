      FUNCTION A1UT1F(JD,FR)

C     THIS FUNCTION COMPUTES A1 - UT1 FOR A GIVEN EPOCH BASED ON THE
C     MEAN RATE OF UT2 AND THE SEASONAL TERMS IN UT1-UT2
C JD - INPUT JULIAN DAY NUMBER ( = [E.T. EPOCH + 0.5] )
C FR - FRACTION OF A DAY SINCE 0H E.T.
C OUTPUT IS IN SECONDS

      IMPLICIT REAL*8(A-H,O-Z)
      parameter (TWOPI=6.28318530717958648d0,npow=4)
	dimension a(npow)
	data a/5.731248d0,3.067635d-3,9.29958655d-10,-1.105026d-11/

      D=(JD-2439857)+FR
      Y=D/365.2421988D0
	ypol=a(1)
	do 100 i=2,npow
100	 ypol=ypol+a(i)*d**(i-1)
      AN1=TWOPI*Y-0.499
      AN2=2D0*TWOPI*Y-0.862
      A1UT1F=ypol+0.025*DSIN(AN1)+0.009*DSIN(AN2)
      RETURN
      END
